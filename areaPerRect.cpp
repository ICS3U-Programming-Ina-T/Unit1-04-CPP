// This program calculates and displays the area and
// perimeter of a rectangle with dimensions 9cm X 4cm.
#include <iostream>

int main() {
  std::cout << "For a rectangle that has dimensions\n";
  std::cout << "length 9cm and width 4cm:\n";
  std::cout << "\n";
  std::cout << "Area = " << 9 * 4 << " cm^2\n";
  std::cout << "Perimeter = " << 2* (9 + 4) << " cm\n";
}
